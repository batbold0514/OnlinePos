package mn.infosystems.estimator.model;

import java.util.Date;

public class EmpReportModel {
	private Integer id;
	private String date;
	private Integer estCount;
	private Long estPrice;
	private Integer insuranceCount;
	private Long insurancePrice;
	
	
	
	public EmpReportModel(){}
	
	public EmpReportModel(Integer id,String date,Integer estCount,Long estPrice,Integer insCount,Long insPrice) {
		this.id = id;
		this.estCount = estCount;
		this.insuranceCount = insCount;
		this.estPrice = estPrice;
		this.insurancePrice = insPrice;
		this.date = date;
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getEstCount() {
		return estCount;
	}
	public void setEstCount(Integer estCount) {
		this.estCount = estCount;
	}
	public Long getEstPrice() {
		return estPrice;
	}
	public void setEstPrice(Long estPrice) {
		this.estPrice = estPrice;
	}
	public Integer getInsuranceCount() {
		return insuranceCount;
	}
	public void setInsuranceCount(Integer insuranceCount) {
		this.insuranceCount = insuranceCount;
	}
	public Long getInsurancePrice() {
		return insurancePrice;
	}
	public void setInsurancePrice(Long insurancePrice) {
		this.insurancePrice = insurancePrice;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}
	
	
}
