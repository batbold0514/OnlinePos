package mn.threesor.tims.model;

import java.util.List;

public class EmployeeSalary {
	private int id;
	private Employee employee;
	private List<StepPriceWithNumber> stepPriceList;
	private int salary;
	
	public Employee getEmployee() {
		return employee;
	}
	public void setEmployee(Employee employee) {
		this.employee = employee;
	}
	
	public int getSalary() {
		return salary;
	}
	public void setSalary(int salary) {
		this.salary = salary;
	}
	public List<StepPriceWithNumber> getStepPriceList() {
		return stepPriceList;
	}
	public void setStepPriceList(List<StepPriceWithNumber> stepPriceList) {
		this.stepPriceList = stepPriceList;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
