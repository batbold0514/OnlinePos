package mn.chinbat.surgery.model;

import java.util.List;

public class MonthBalanceModel {
	private int orderNumber;
	private Doctor doctor;
	private List<MonthMap> priceMapList;
	private int totalPrice;
	private Integer number;
	private String duration;
	private Patient patient;
	private int paidValue;
	private int directValue; // шууд төлбөр
	private int recoupment; // нөхөн төлбөр
	
	public Integer getNumber() {
		return number;
	}

	public void setNumber(Integer number) {
		this.number = number;
	}

	public Doctor getDoctor() {
		return doctor;
	}

	public void setDoctor(Doctor doctor) {
		this.doctor = doctor;
	}

	public int getTotalPrice() {
		return totalPrice;
	}

	public void setTotalPrice(int totalPrice) {
		this.totalPrice = totalPrice;
	}

	public List<MonthMap> getPriceMapList() {
		return priceMapList;
	}

	public void setPriceMapList(List<MonthMap> priceMapList) {
		this.priceMapList = priceMapList;
	}

	public String getDuration() {
		return duration;
	}

	public void setDuration(String duration) {
		this.duration = duration;
	}

	public int getOrderNumber() {
		return orderNumber;
	}

	public void setOrderNumber(int orderNumber) {
		this.orderNumber = orderNumber;
	}

	public Patient getPatient() {
		return patient;
	}

	public void setPatient(Patient patient) {
		this.patient = patient;
	}

	public int getPaidValue() {
		return paidValue;
	}

	public void setPaidValue(int paidValue) {
		this.paidValue = paidValue;
	}

	public int getDirectValue() {
		return directValue;
	}

	public void setDirectValue(int directValue) {
		this.directValue = directValue;
	}

	public int getRecoupment() {
		return recoupment;
	}

	public void setRecoupment(int recoupment) {
		this.recoupment = recoupment;
	}
	
}
